export type AppLanguage = 'pt' | 'en'

export function getLanguageFromLocale(locale?: string): AppLanguage {
  const value = (locale || '').toLowerCase()

  if (value.startsWith('pt')) return 'pt'
  if (value.startsWith('en')) return 'en'

  return 'en'
}

